package com.LBA.Advertiser.bean;

public class AdvertisementBean {

	public String fileName;
	public String adName;
	public String adDesc;
	public String adStartDate;
	public String adEndDate;
	public String productID;
	public String channelID;
	public String contractID;
	public String fileLocation;
	/**
	 * @return the fileName
	 */
	public String getFileName() {
		return fileName;
	}
	/**
	 * @param fileName the fileName to set
	 */
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	/**
	 * @return the adName
	 */
	public String getAdName() {
		return adName;
	}
	/**
	 * @param adName the adName to set
	 */
	public void setAdName(String adName) {
		this.adName = adName;
	}
	/**
	 * @return the adDesc
	 */
	public String getAdDesc() {
		return adDesc;
	}
	/**
	 * @param adDesc the adDesc to set
	 */
	public void setAdDesc(String adDesc) {
		this.adDesc = adDesc;
	}
	/**
	 * @return the adStartDate
	 */
	public String getAdStartDate() {
		return adStartDate;
	}
	/**
	 * @param adStartDate the adStartDate to set
	 */
	public void setAdStartDate(String adStartDate) {
		this.adStartDate = adStartDate;
	}
	/**
	 * @return the adEndDate
	 */
	public String getAdEndDate() {
		return adEndDate;
	}
	/**
	 * @param adEndDate the adEndDate to set
	 */
	public void setAdEndDate(String adEndDate) {
		this.adEndDate = adEndDate;
	}
	/**
	 * @return the productID
	 */
	public String getProductID() {
		return productID;
	}
	/**
	 * @param productID the productID to set
	 */
	public void setProductID(String productID) {
		this.productID = productID;
	}
	/**
	 * @return the channelID
	 */
	public String getChannelID() {
		return channelID;
	}
	/**
	 * @param channelID the channelID to set
	 */
	public void setChannelID(String channelID) {
		this.channelID = channelID;
	}
	/**
	 * @return the contractID
	 */
	public String getContractID() {
		return contractID;
	}
	/**
	 * @param contractID the contractID to set
	 */
	public void setContractID(String contractID) {
		this.contractID = contractID;
	}
	/**
	 * @return the fileLocation
	 */
	public String getFileLocation() {
		return fileLocation;
	}
	/**
	 * @param fileLocation the fileLocation to set
	 */
	public void setFileLocation(String fileLocation) {
		this.fileLocation = fileLocation;
	}
	
}
